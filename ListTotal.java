import java.util.List;
import java.util.ArrayList;



public class ListTotal
{
 public static int total( List<Integer> ray )
 {
   //return 0;
    int sum = 0; 
    for(int i = 0; i < ray.size(); i++){
      sum += ray.get(i); 
      return sum;
    }
  }
}

 
  
 





